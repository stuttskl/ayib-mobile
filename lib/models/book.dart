  final String title;
  final String author;
  final String description;
  final double rating;
  final String imgUrl;
  final String startDate;
  final String endDate;
  final bool current;

  Book(
      {this.title,
      this.author,
      this.description,
      this.rating,
      this.imgUrl,
      this.startDate,
      this.endDate,
      this.current});
}
